#TUPLAS - ORDENADA E IMUTÁVEL;
minhaTupla = (1, 2, 2, 3, 4)

print("Minha tupla", minhaTupla)

#Acessando posição 3 da tupla
print("Acessando segunda posição da tupla = ", minhaTupla[2])

#Método COUNT
contagem = minhaTupla.count(2)
print("Nº 2 repete X vezes: x = ", contagem)

#Método INDEX
indice = minhaTupla.index(3)
print("Indice do elemento 3: ", indice)