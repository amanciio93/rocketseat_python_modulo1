# type() - função que retorna o tipo de variável

# Inteiro
numeroInteiro = 3
print("Inteiro = ", numeroInteiro)
print("Tipo da variável = ", type(numeroInteiro))

# Real com ponto flutuante
numeroReal = 3.34
print("Real com ponto flutuante = ", numeroReal)
print("Tipo da variável = ", type(numeroReal))