#LISTAS ou ARRAYS
minhaLista = [1, 2, 3, 4, 5, "Jonathan", False]

#Exibindo lista
print(minhaLista)

#Exibindo elemento posição 1 = 2
print("minhaLista[1] = ", minhaLista[1])

#Exibindo elementos da posição 1 a 4
print("minhaLista[1:5]", minhaLista[1:5])

# Exibindo elementos de 0 até o alvo;
print("minhaLista[Inicio até o alvo] = ", minhaLista[:6])

#Exibindo elementos do alvo até o final;
print("minhaLista[Alvo até o final] = ", minhaLista[4:])